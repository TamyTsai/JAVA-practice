// 變數

void main() {
	// 靜態型別 程式撰寫時需明確的型別宣告
	// 字串: "小綠"
	String name = "小綠"; // 要先定義 後面的程式碼才能使用變數
	System.out.println(name);
	name = "小黃"; // 變數內的值改變
	System.out.println(name);
	// 字元: '王'
	char lastName = '王';
	System.out.println(lastName);
	lastName = '黃';
	System.out.println(lastName);
	// 整數: 18
	int age = 18;
	System.out.println(age);
	age = 25;
	System.out.println(age);
	// 浮點數: 160.5
	double height = 160.5;
	System.out.println(height);
	// 布林值: true false 
	boolean isMale = true;
	System.out.println(isMale);
	isMale = false;
	System.out.println(isMale);
}

